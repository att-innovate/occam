zone_file = '/etc/occam/zone'
Facter.add("zone") do
  setcode do
    if File.exists?(zone_file)
      # set up zone fact from file (if exists)
      File.read(zone_file).chop
    else
      # second value in a fqdn is the zone, by convention
      # dns1.zion.tfoundry.com has a zone of 'zion'
      Facter.value('fqdn').split(".")[1]
    end
  end
end
